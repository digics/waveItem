import  time
import  sys

####################################################

def exit( code = 0 ):

    print()
    sys.exit( code )


####################################################

def fatal( text ):

    print()
    print( "fatal: " + text )
    print()

    exit(1)

####################################################

def warning( t ):

    print( f"warning: {t}" )

####################################################

def getime():

    return time.time()


